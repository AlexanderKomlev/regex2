import phoneСorrector from '../app';

test.each([
  ['+7(999)999-99-99', '+79999999999'],
  ['8 (999)999-99-99', '+79999999999'],
  ['+7 (999)9999999', '+79999999999'],
  ['+7999999-99-99', '+79999999999'],
  ['8(999)9999999', '+79999999999'],
  ['+7 (999) 999 99 99', '+79999999999'],
  ['8999 999-99-99', '+79999999999'],
  ['+7(999)999-9999', '+79999999999'],
  ['+7 999 999 99 99', '+79999999999'],
  ['+7 9999999999', '+79999999999'],
  ['+7-999-999-99-99', '+79999999999'],
  ['+7 999 9999999', '+79999999999'],
  ['+8(999)999-9999', '+79999999999'],
  ['+86000 000 0000', '+860000000000'],
  ['+86 000 000 0000', '+860000000000'],
  ['+86-000-000-00-00', '+860000000000'],
])('%o -> %o', (phone, result) => {
  expect(phoneСorrector(phone)).toBe(result);
});
